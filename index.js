// Similar to setting a Ruby variable, we set a variable in JS using 'let'
let count = 12
//We can then view this variable with the syntax below
console.log(count)
// For example
let myAge = 28
console.log(myAge)

// We can also do arithmetics using *, / and -
let count = 5 + 2
console.log(count)
// This can also be done with variables
let firstBatch = 5
let secondBatch = 7
let count = firstBatch + secondBatch
console.log(count)
// For example
let myAge = 28
let humanDogRatio = 7
let myDogAge = myAge * humanDogRatio
console.log(myDogAge)